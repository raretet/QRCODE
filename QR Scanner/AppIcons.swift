struct PlusBadgeShape: Shape {
    func path(in rect: CGRect) -> Path {
        var path = Path(roundedRect: rect.insetBy(dx: 3, dy: 3), cornerSize: CGSize(width: 6, height: 6))
        path.move(to: CGPoint(x: rect.midX, y: rect.minY + 7))
        path.addLine(to: CGPoint(x: rect.midX, y: rect.maxY - 7))
        path.move(to: CGPoint(x: rect.minX + 7, y: rect.midY))
        path.addLine(to: CGPoint(x: rect.maxX - 7, y: rect.midY))
        return path
    }
}

struct ScanCornersShape: Shape {
    func path(in rect: CGRect) -> Path {
        var path = Path()
        let inset = rect.width * 0.12
        let short = rect.width * 0.22
        path.move(to: CGPoint(x: inset + short, y: inset))
        path.addLine(to: CGPoint(x: inset, y: inset))
        path.addLine(to: CGPoint(x: inset, y: inset + short))
        path.move(to: CGPoint(x: rect.maxX - inset - short, y: inset))
        path.addLine(to: CGPoint(x: rect.maxX - inset, y: inset))
        path.addLine(to: CGPoint(x: rect.maxX - inset, y: inset + short))
        path.move(to: CGPoint(x: inset, y: rect.maxY - inset - short))
        path.addLine(to: CGPoint(x: inset, y: rect.maxY - inset))
        path.addLine(to: CGPoint(x: inset + short, y: rect.maxY - inset))
        path.move(to: CGPoint(x: rect.maxX - inset - short, y: rect.maxY - inset))
        path.addLine(to: CGPoint(x: rect.maxX - inset, y: rect.maxY - inset))
        path.addLine(to: CGPoint(x: rect.maxX - inset, y: rect.maxY - inset - short))
        return path
    }
